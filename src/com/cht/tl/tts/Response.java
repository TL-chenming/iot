package com.cht.tl.tts;

public class Response {
	private String status;
	private String file;
	
	public void setStatus(String value) {
		status = value;
	}
	
	public String getStatus() {
		return status;
	}
	
	public void setFile(String value) {
		file = value;
	}
	
	public String getFile() {
		return file;
	}
}
